<?php namespace Studiz\Core\ORM;

/**
 * Class GenericORM
 *
 * This GenericORM is should be the base
 * for all your package models.
 *
 * @author Selcuk Kekec <senycorp@googlemail.com>
 * @package Studiz\Core\ORM
 */
class GenericORM extends CoreORM
{
    /**
     * Factory a new instance of model
     *
     * @param array $attributes
     * @return static
     */
    public static function factory(array $attributes = array())
    {
        return new static($attributes);
    }

    public static function whereMod($column, $operator = null, $value = null, $boolean = 'and')
    {
        if (is_array($column))
        {
            $instance = new static;

            foreach ($column as $field => $value)
            {
                $instance->where($field, ' = ', $value);
            }

            return $instance;
        }
        else
        {
            return static::where($column, $operator, $value, $boolean); // TODO: Change the autogenerated stub
        }
    }


}